/**
 * 최종 능력치를 바탕으로 엔딩을 계산합니다.
 * @param {Object} stats - gameState.stats (공개+숨김 능력치 모두 포함)
 * @returns {Object} ENDINGS 안의 엔딩 객체 하나
 */
function calculateEnding(stats) {
  // 숨겨진 능력치(불만)의 총합 - 값이 클수록 조정/백성/유생 모두가 불만족 상태
  const totalDiscontent = stats.gwijok + stats.baekseong + stats.yusaeng;

  // 1. 불만이 극도로 높은데 황권까지 강하게 억누른 경우 -> 폭군 엔딩(반란 위험)
  if (totalDiscontent >= 170 && stats.wangkwon >= 55) {
    return ENDINGS.pokgun;
  }

  // 2. 황권 자체가 너무 낮아진 경우 -> 환관/측근이 실권을 쥔 허수아비 엔딩
  if (stats.wangkwon <= 35) {
    return ENDINGS.goerae;
  }

  // 3. 민심과 재정이 모두 좋고 불만이 낮은 경우 -> 통일과 절제의 황제 엔딩
  if (stats.minsim >= 62 && stats.jaejeong >= 52 && totalDiscontent < 140) {
    return ENDINGS.seonggun;
  }

  // 4. 실리는 챙겼지만 유생 반발 등이 큰 경우 -> 위대한 개혁, 무거운 대가 엔딩
  if (stats.yusaeng >= 55 || (stats.gunsa >= 60 && totalDiscontent >= 140)) {
    return ENDINGS.gaehyeok;
  }

  // 5. 그 외 -> 균형 위에 선 제국 엔딩 (기본값)
  return ENDINGS.gyunhyeong;
}
